#!/bin/bash

# Hybrid RAG 停止脚本

echo "=========================================="
echo "  停止 Hybrid RAG 服务"
echo "=========================================="

# 停止后端
echo "🛑 停止后端服务..."
pkill -f uvicorn 2>/dev/null && echo "   ✅ 后端已停止" || echo "   ⚠️  后端未在运行"

# 停止前端
echo "🛑 停止前端服务..."
pkill -f vite 2>/dev/null && echo "   ✅ 前端已停止" || echo "   ⚠️  前端未在运行"

echo ""
echo "=========================================="
echo "  ✅ 所有服务已停止"
echo "=========================================="
